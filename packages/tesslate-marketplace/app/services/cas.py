# S3 adapter — boto3 shape, no hard dep at import time
# ---------------------------------------------------------------------------


class S3BundleStorage:
    """S3-backed bundle storage. Lazily imports boto3."""

    backend_name = "s3"

    def __init__(self, settings: Settings) -> None:
        if not settings.s3_bucket:
            raise RuntimeError("BUNDLE_STORAGE_BACKEND=s3 requires S3_BUCKET")
        try:
            import boto3  # type: ignore[import-not-found]
        except ImportError as exc:
            raise RuntimeError(
                "S3 bundle storage requires the optional `boto3` dependency"
            ) from exc

        self._settings = settings
        self._bucket = settings.s3_bucket
        kwargs: dict = {}
        if settings.s3_region:
            kwargs["region_name"] = settings.s3_region
        if settings.s3_endpoint_url:
            kwargs["endpoint_url"] = settings.s3_endpoint_url
        self._client = boto3.client("s3", **kwargs)

    def _key_for(self, kind: str, slug: str, version: str) -> str:
        return f"{_safe_segment(kind)}/{_safe_segment(slug)}/{_safe_segment(version)}.tar.zst"

    def put_bytes(self, kind: str, slug: str, version: str, data: bytes) -> BundleRef:
        key = self._key_for(kind, slug, version)
        self._client.put_object(
            Bucket=self._bucket,
            Key=key,
            Body=data,
            ContentType="application/zstd",
        )
        return BundleRef(
            storage_key=key,
            sha256=hashlib.sha256(data).hexdigest(),
            size_bytes=len(data),
            backend=self.backend_name,
        )

    def open_stream(self, storage_key: str):
        obj = self._client.get_object(Bucket=self._bucket, Key=storage_key)
        return obj["Body"]

    def head(self, storage_key: str) -> dict[str, int | str]:
        head = self._client.head_object(Bucket=self._bucket, Key=storage_key)
        return {"size_bytes": head["ContentLength"], "storage_key": storage_key}

    def signed_url(self, kind: str, slug: str, version: str, storage_key: str) -> tuple[str, int]:
        ttl = self._settings.bundle_url_ttl_seconds
        url = self._client.generate_presigned_url(
            "get_object",
            Params={"Bucket": self._bucket, "Key": storage_key},
            ExpiresIn=ttl,
        )
        return url, int(time.time()) + ttl


# ---------------------------------------------------------------------------
# Volume Hub adapter — gRPC bridge for the OpenSail Volume Hub.
# Wave-7 documented placeholder; raises NotImplementedError if no URL set.
# ---------------------------------------------------------------------------


class VolumeHubBundleStorage:
    """Fronts the OpenSail Volume Hub for app-bundle CAS reads."""

    backend_name = "volume_hub"

    def __init__(self, settings: Settings) -> None:
        if not settings.volume_hub_url:
            raise NotImplementedError(
                "Volume Hub adapter requires VOLUME_HUB_URL; not yet wired in this build"
            )
        self._settings = settings
        self._endpoint = settings.volume_hub_url.rstrip("/")

    def put_bytes(self, kind: str, slug: str, version: str, data: bytes) -> BundleRef:
        raise NotImplementedError("Volume Hub put is owned by the orchestrator-side publisher")

    def open_stream(self, storage_key: str):
        raise NotImplementedError("Volume Hub stream reads are out of scope for the marketplace service")

    def head(self, storage_key: str) -> dict[str, int | str]:
        raise NotImplementedError("head not implemented for Volume Hub adapter")

    def signed_url(self, kind: str, slug: str, version: str, storage_key: str) -> tuple[str, int]:
        # In production this asks Volume Hub for a pre-signed S3 URL via gRPC.
        # We keep the method available so the rest of the pipeline doesn't
        # branch on backend; orchestrators that opt into this backend deploy
        # the gRPC client wiring out-of-band.
        raise NotImplementedError(
            "Volume Hub signed URL generation requires the gRPC client; not enabled in this build"
        )


# ---------------------------------------------------------------------------
# Factory
# ---------------------------------------------------------------------------


_storage: BundleStorage | None = None


def get_bundle_storage(settings: Settings | None = None) -> BundleStorage:
    global _storage
    if _storage is not None:
        return _storage
    settings = settings or get_settings()
    backend = settings.bundle_storage_backend.lower()
    if backend == "local":
        _storage = LocalBundleStorage(settings)
    elif backend == "s3":
        _storage = S3BundleStorage(settings)
    elif backend == "volume_hub":
        _storage = VolumeHubBundleStorage(settings)
    else:
        raise RuntimeError(f"unknown BUNDLE_STORAGE_BACKEND={backend!r}")
    return _storage


def reset_bundle_storage_cache() -> None:
    global _storage
    _storage = None
