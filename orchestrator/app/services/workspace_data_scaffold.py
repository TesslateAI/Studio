"""Auto-scaffold a Workspace Data Store SDK helper into newly-created projects.

Drops a correct, canonical client (`lib/opensailData.{ts,py}`) + a
``.env.example`` into the project at creation time, so the AI agent has a
real file to import — not a spec to hallucinate from. Detects the project's
stack from its files and picks the right template. Fully guarded: failure
here never blocks project creation.
"""

