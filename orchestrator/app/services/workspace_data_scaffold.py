async def _read(orchestrator, project, user_id: UUID, path: str) -> str | None:
    try:
        return await orchestrator.read_file(
            user_id=user_id,
            project_id=project.id,
            container_name=None,
            file_path=path,
            project_slug=project.slug,
            volume_id=getattr(project, "volume_id", None),
            cache_node=getattr(project, "cache_node", None),
        )
    except Exception:
        return None


async def _detect_stack(orchestrator, project, user_id: UUID) -> str:
    """Return one of: 'nextjs', 'vite', 'plain_js', 'python', 'empty'."""
    pkg = await _read(orchestrator, project, user_id, "package.json")
    if pkg:
        try:
            data = json.loads(pkg)
            deps = {**(data.get("dependencies") or {}), **(data.get("devDependencies") or {})}
            if "next" in deps:
                return "nextjs"
            if "vite" in deps:
                return "vite"
            return "plain_js"
        except json.JSONDecodeError:
            pass
    for marker in ("pyproject.toml", "requirements.txt"):
        if await _read(orchestrator, project, user_id, marker):
            return "python"
    return "empty"


_FILES_PER_STACK: dict[str, list[tuple[str, str]]] = {
    "nextjs": [("lib/opensailData.ts", _NEXTJS_TS), (".env.example", _NEXTJS_ENV)],
    "vite": [("src/lib/opensailData.ts", _VITE_TS), (".env.example", _VITE_ENV)],
    "plain_js": [("lib/opensailData.ts", _NEXTJS_TS), (".env.example", _NEXTJS_ENV)],
    "python": [("opensail_data.py", _PYTHON_PY), (".env.example", _PYTHON_ENV)],
    "empty": [("OPENSAIL_DATA.md", _GENERIC_README)],
}


async def scaffold_workspace_data(project: Any, user_id: UUID, settings: Any) -> None:
    """Drop the Workspace Data SDK helper(s) into the project.

    Detects the stack from project files and writes the language-appropriate
    SDK + ``.env.example`` (or a generic README for empty workspaces). Skips
    any file that already exists so we never overwrite the user's edits or
    files baked into the base. Fully guarded — never raises.
    """
    try:
        from .orchestration import get_orchestrator

        orchestrator = get_orchestrator()
        stack = await _detect_stack(orchestrator, project, user_id)
        files = _FILES_PER_STACK.get(stack, _FILES_PER_STACK["empty"])
        for path, content in files:
            if await _read(orchestrator, project, user_id, path) is not None:
                logger.info(
                    "[ws-data-scaffold] %s already exists in project %s, skipping",
                    path,
                    project.id,
                )
                continue
            try:
                await orchestrator.write_file(
                    user_id=user_id,
                    project_id=project.id,
                    container_name=None,
                    file_path=path,
                    content=content,
                    project_slug=project.slug,
                    volume_id=getattr(project, "volume_id", None),
                    cache_node=getattr(project, "cache_node", None),
                )
                logger.info(
                    "[ws-data-scaffold] wrote %s for project %s (stack=%s)",
                    path,
                    project.id,
                    stack,
                )
            except Exception as exc:
                logger.warning(
                    "[ws-data-scaffold] write %s failed for project %s: %s",
                    path,
                    project.id,
                    exc,
                )
    except Exception as exc:
        logger.warning(
            "[ws-data-scaffold] failed for project %s: %s",
            getattr(project, "id", "?"),
            exc,
        )
